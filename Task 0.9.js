function printOutVowels()
let vowelsCount = 0;
for (let i = 0; i<printOutVowels.length; i++){
    switch (printOutVowels[i]){
        case 'a':
        case 'A':
        case 'e':
        case 'E':
        case 'i':
        case 'I':
        case 'o':
        case 'O':
        case 'u':
        case 'U':
            vowelsCount++
            break;
    }
}